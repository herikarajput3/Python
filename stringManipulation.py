# String concatenation
first_name = 'Herika'
last_name = 'Rajput'

full_name = first_name + ' ' + last_name
print(full_name)

# String multiplication
a = 'Herika'
print((a + " ") * 5)

# String Appending with += operator
name = 'Herika'
name += ' Rajput'
print(name)

# String Length
name = 'Herika'
print(f"Length of name: {len(name)}")

# String Finding
name = 'Herika'
print(f"Find k in name: {name.find('k')}")

# Changing Case
name = 'herika rajput'
print(f"Uppercase: {name.upper()}")
print(f"Lowercase: {name.lower()}")
print(f"Capitalize: {name.capitalize()}")

